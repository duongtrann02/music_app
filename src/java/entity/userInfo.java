package entity;

/**
 *
 * @author Hi
 */
public class userInfo {
    int id;
    String name;

    public userInfo() {
    }

    public userInfo(String name) {
        this.name = name;
    }

    public userInfo(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
